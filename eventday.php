</ul>
<br/> 


<b>If you are arriving by BART:</b><br />
<ul>
<li>Get off at Downtown Berkeley station</li>
<li>Walk east on Center Street for 1 block to the campus. Enter campus at Oxford Street and Center Street.</li>
<li>Take the Path on the right through the Eucalyptus Grove and make a left at Campanile Way.</li>
<li>Continue to walk toward Sather Tower, which is the large bell tower on campus.</li>
<li>Find the balloon arch! This is the start booth where your adventure will begin.</li>
</ul>
<br/>
