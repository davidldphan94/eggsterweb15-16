For those of you driving to our event, please be forewarned that parking in Berkeley is notoriously hard. There is an underground parking garage on the north side of Bancroft west of Telegraph.  If that is full, there are spaces along the streets.  You may not park in residential areas of Berkeley for more than 2 hours if you do not have a Residential Parking Permit (RPP).  Click <a href="http://pt.berkeley.edu" target="_blank">here</a> for more on parking in Berkeley. 
