<li>Continue onto 51st St</li>  
<li>Turn left at Telegraph Ave</li>
<li>Turn left on Bancroft and you will reach the North side of campus. There are <a href="http://pt.berkeley.edu/park/public/campus">parking structures</a> in the vicinity or you may look for street parking.</li>
</ul>


<br/>
