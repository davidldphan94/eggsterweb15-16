                    <p>The Inder and Uma Batra Memorial Fund was created to support the Eggster Hunt and Learning Festival at UC Berkeley, a non-profit UC Berkeley-student run organization whose mission is to promote higher education and the well-being of underprivileged and disabled children. Each year, Eggster organizes a free learning festival and the largest free egghunt in Northern California for Bay Area youth. The organization also awards grants annually to three local nonprofit children&#39s agencies. All donations received towards the Batra Memorial Fund will be given to Eggster to support its mission. Anyone may contribute to the fund. The Batra Memorial Fund was established by alumni members of the Eggster Hunt and Learning Festival in 2009 in memory of Inder and Uma Batra. Inder and Uma Batra died in an automobile accident on July 23, 2007. They are survived by two daughters, Mala and Puja Batra.</p>
                    <p>Inder and Uma Batra held a lifelong commitment to community service. Inder Batra was a respected scholar, having completed his M.S. in Physics and doctoral degree. He worked as a physicist at IBM Research Lab for almost 30 years. However, only once he became the head of the Physics Department at the University of Illinois at Chicago did he find his true calling. As his family writes, &#34life at UIC was a dream come true, because in addition to making a scientific contribution, he could make an impact on young, aspiring physicists.&#34 Inder was determined to pass on the gift of education to so many young minds. Just like her life partner, Uma Batra found her life&#39s calling in giving back to others. As a registered dietician for almost 30 years, she spent many of these years volunteering in local and Chicago area hospitals. Reflecting on her life&#39s work, Uma&#39s family writes that &#34working with patients for so many years also contributed to the gentleness she brought to all of her relationships.&#34</p>
                    <p>
                    Inder and Uma Batra&#39s commitment to helping others, through education and health and wellness, has reflected itself in their daughters, Puja and Mala. Puja, a graduate of Harvard Law School, and Mala, a graduate of Harvard Business School, have spent much of their personal and professional lives giving back to others. While at UC Berkeley, Mala was a dedicated member of the Eggster Hunt and Learning Festival. Participating in the organization in various capacities, she co-chaired the organization&#39s Community Relations and Finance committees, and then became one of the organization&#39s overall co-chairs for the 2000-2001 school year. Inder and Uma Batra&#39s unwavering support of their daughter&#39s commitment to Eggster was evident year after year. Former Eggster members remember seeing them the eve before the big day in the basement of the UC Berkeley Alumni House, brightening the room with their smiles and lending a hand wherever one was needed, or walking near Strawberry Creek Canyon, enjoying all the festivities on the event day.</p><p>
                    The Inder and Uma Batra Memorial Fund honors the lives of these two remarkable individuals who showed tremendous generosity to the Eggster Hunt and Learning Festival. It is our hope that the Batra Memorial Fund will serve as remembrance of the deep spirit of giving Inder and Uma shared with the Eggster Hunt and Learning Festival and all those in their lives.
