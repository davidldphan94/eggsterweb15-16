					<div>The <span style="color:#267266">Finance Committee</span> takes on all fiscal responsibilities. In addition to doing organizational bookkeeping such as <b>tracking the budget</b> and <b>processing reimbursements</b>, they also work to <b>acquire prize donations</b> for the winners of the raffle drawing held at every event. Finance is also in charge of <b>writing grant and corporate applications</b>, which constitute much of Eggster's budget.
<br />
<br />
The <span style="color:#267266">Event Committee</span> accounts for just that, the event. Every year, their creative minds come up with a <b>theme</b> and about 30 corresponding booths for the children to enjoy. They not only <b>research and create learning and activity booths</b>, but also acquire the necessary materials and supplies through in-kind donations. They also <b>train the 300 volunteers</b> through orientations about a month before the big day. They design the egg hunts and have recently added obstacle courses to physically challenge the children.
<br />
<br />	<img src="css/images/inv2.jpg" alt="" class="left" />
The <span style="color:#267266">Public Community Relations Committee</span> (PCR) consists of the recent combinations of two committees: Public Relations (PR) and Community Relations (Comrel).  PCR deals with both the public and the community aspects of the organization. PCR advertise for our event directly to low-income elementary schools, and are involved throughout the entire process of inviting, choosing, and working with our beneficiaries.  PCR is also responsible for attaining food and candy donations for the egg hunt.  In doing so we work with small businesses as well as large corporations. PCR networks with elementary schools, small businesses, corporations, and non-profits in the Bay Area. 
<br />
<br />
The <span style="color:#267266">Social Historian and Technology chair</span> records minutes, organizes social events, and compiles a scrapbook at the end of the year. SHA also designs and periodically updates Eggster's website, which is essential for communication within the student group and with the greater university and Bay Area community.
