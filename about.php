        <div class="page-header">
            <h3>Mission</h3>
        </div>
        <p>
            Eggster's mission is to nurture the creativity, curiosity, and welfare of Bay Area youth by:
            <ul>
            <li>offering a free event on the UC Berkeley campus open to all families and providing children with a fun and educational experience through egg hunts, learning booths, and performances while simultaneously introducing them to the benefits of a post-secondary education;</li>
            <li>bringing together corporations, local businesses, UC Berkeley alumni and students, and other members of the Bay Area community in the interest of supporting select youth oriented non-profit agencies;</li>
            <li>uniting UC Berkeley organizations, departments, and students in putting on the event and serving the local community;</li>
            <li>volunteering in the community to connect with the population we serve.</li>
            </ul>
        </p>
        <div class="page-header">
            <h3>About the Event</h3>
        </div>
        <p>
                   The Eggster Egghunt and Learning Festival is a one-day carnival of fun and learning, open to the Bay Area community for free. Last year, we had over 1,000 children and their families come and enjoy the egg hunts, entertainment and more than 40 learning booths organized by over 300 student volunteers. We raised close to $10,000 through individual and corporate donations. Proceeds from the event went toward local nonprofit children's agencies. The festival is wheelchair accessible, and there are special egg hunts for young toddlers and for children in wheelchairs. Eggster Organization is a student group acting independently of the University of California. The student organization is the host of Eggster's 22nd Annual Egghunt and Learning Festival and takes full responsibility for the guests.
               </p>
        <div id="committees" class="page-header">
            <h3>Egghead Positions and Committees</h3>
        </div>

            <p>
    			Each committee is made up of two leaders ("Eggheads") and a handful of members.
				The two "Overalls" are the leaders in charge of the "Eggheads".
			</p>
			
            <p>
				The <b>Finance Committee</b> takes on all fiscal responsibilities. In addition to doing organizational bookkeeping such as <b>tracking the budget</b> and <b>processing reimbursements</b>, they also work to <b>acquire prize donations</b> for the winners of the raffle drawing held at every event. Finance is also in charge of <b>writing grant and corporate applications</b>, which constitute much of Eggster's budget.
            </p>
            
            <p>
                The <b>Event Committee</b> accounts for just that, the event. Every year, their creative minds come up with a <b>theme</b> and about 30 corresponding booths for the children to enjoy. They not only <b>research and create learning and activity booths</b>, but also acquire the necessary materials and supplies through in-kind donations. They also <b>train the 300 volunteers</b> through orientations about a month before the big day. They design the egg hunts and have recently added obstacle courses to physically challenge the children.
            </p>	

            <p>
                The <b>Public Community Relations Committee</b> (PCR) consists of the recent combinations of two committees: Public Relations (PR) and Community Relations (Comrel).  PCR deals with both the public and the community aspects of the organization. PCR advertise for our event directly to low-income elementary schools, and are involved throughout the entire process of inviting, choosing, and working with our beneficiaries.  PCR is also responsible for attaining food and candy donations for the egg hunt.  In doing so we work with small businesses as well as large corporations. PCR networks with elementary schools, small businesses, corporations, and non-profits in the Bay Area. 
            </p>

            <p>
                The <b>Social Historian and Alumni Relations chair</b> records minutes, organizes social events, and compiles a scrapbook at the end of the year. SHA also periodically updates and gets into contact with Eggster alumni by sending out a newsletter every year.
            </p>


